#include "3-calc.h"

/**
 * op_add - add 2 integers
 * @a: first int
 * @b: second int
 * Return: sum of 2 integers.
*/
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - sub 2 integers
 * @a: first int
 * @b: second int
 * Return: sub of 2 integers.
*/
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiple 2 integers
 * @a: first int
 * @b: second int
 * Return: multiple of 2 integers.
*/
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - divsion 2 integers
 * @a: first int
 * @b: second int
 * Return: disvsion of 2 integers.
*/
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - mod 2 integers
 * @a: first int
 * @b: second int
 * Return: mod of 2 integers.
*/
int op_mod(int a, int b)
{
	return (a % b);
}
